export const POSTED_TIMES = ["2 hours ago","5 hours ago","Yesterday","2 days ago","3 days ago","4 days ago","1 week ago","2 weeks ago"];

function generateJobs() {
  const jobs = []; let id = 1; const target = 1050; let iter = 0;
  while (jobs.length < target) {
    for (const tmpl of JOB_TEMPLATES) {
      if (jobs.length >= target) break;
      const company = COMPANIES[(id * 7 + iter * 3) % COMPANIES.length];
      const location = LOCATIONS[(id * 5 + iter * 11) % LOCATIONS.length];
      const typeOpts = tmpl.level === "Intern" ? ["Internship","Part-Time","Remote"]
        : tmpl.level === "Executive" || tmpl.level === "VP" ? ["Full-Time"] : TYPES;
      const type = typeOpts[(id * 3 + iter) % typeOpts.length];
      const applicants = 5 + ((id * 13 + iter * 7) % 195);
      const posted = POSTED_TIMES[(id + iter) % POSTED_TIMES.length];
      jobs.push({
        id: String(id), title: tmpl.title, company: company.name, logo: company.logo,
        logoBg: company.logoBg, logoColor: company.logoColor, location, type,
        salary: tmpl.salary, field: tmpl.field, level: tmpl.level,
        description: `${company.name} is hiring a ${tmpl.title} to join our growing ${tmpl.field} team. This is an exciting opportunity to work at one of the most innovative companies in the industry.\n\nYou'll collaborate closely with cross-functional teams to ship high-quality work that impacts millions of users. We value ownership, clarity, and craft.\n\nWhether you're early in your career or a seasoned pro, you'll find a place to grow, contribute meaningfully, and build something you're proud of.`,
        requirements: [
          tmpl.level === "Intern" ? "Currently pursuing a relevant degree" : `${tmpl.level}-level experience in ${tmpl.field}`,
          "Strong communication and collaboration skills",
          `Passion for ${tmpl.field} and continuous learning`,
          tmpl.level === "Intern" ? "Eager to learn in a fast-paced environment" : "Proven track record of delivering impactful projects",
        ],
        posted, applicants,
      });
      id++;
    }
    iter++;
  }
  return jobs;
}

export const ALL_JOBS = generateJobs();
